class PokerHand
  HAND_SIZE = 5

  attr_reader :hand

  def initialize(deck)
    @hand = []
    HAND_SIZE.times { @hand << deck.draw }
  end

  def print
    puts hand
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def royal_flush?
    ranks = ['Ace', 'King', 'Queen', 'Jack', 10]
    hand.all? do |card|
      ranks.include?(card.rank) &&
      hand.first.suit == card.suit
    end
  end

  def straight_flush?
    hand.all? { |card| hand.first.suit == card.suit } &&
    Card::RANKING.each_cons(5).any? do |subset|
      subset == hand.sort.map(&:rank)
    end
  end

  def four_of_a_kind?
    hand.combination(4).any? do |subset|
      subset.uniq { |card| card.rank }.size == 1
    end
  end

  def full_house?
    hand.uniq { |card| card.rank }.size == 2
  end

  def flush?
    hand.all? { |card| hand.first.suit == card.suit }
  end

  def straight?
    Card::RANKING.each_cons(5).any? do |subset|
      subset == hand.sort.map(&:rank)
    end
  end

  def three_of_a_kind?
    hand.combination(3).any? do |subset|
      subset.uniq { |card| card.rank }.size == 1
    end
  end

  def two_pair?
    hand.uniq { |card| card.rank }.size == 3
  end

  def pair?
    hand.combination(2).any? do |subset|
      subset.uniq { |card| card.rank }.size == 1
    end
  end
end

